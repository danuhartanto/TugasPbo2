public class pbo {
    public void perkalian (int i, int n){
            int b;
            b= i*n;
            System.out.print("hasil : "+b);
        }
    public void penjumlahan (int o, int p){
            int b;
            b= o+p;
            System.out.print("hasil : "+b);
        }
    public void penngurangan (int k, int l){
            int b;
            b= k-l;
            System.out.print("hasil : "+b);
        }
    public void pembagian (int x, int y){
            int b;
            b= x / y;
            System.out.print("hasil : "+b);
        }
    public void modulus (int m, int n){
            int b;
            b= m % n;
            System.out.print("hasil : "+b);
        }
}
